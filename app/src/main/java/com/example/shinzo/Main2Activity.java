    private void Logout(){
        firebaseAuth = FirebaseAuth.getInstance();
        firebaseAuth.signOut();
        finish();
        startActivity(new Intent(Main2Activity.this, MainActivity.class));
    }

    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.nav_profile) {
            Intent i = new Intent(Main2Activity.this, ProfileActivity.class);
            startActivity(i);

        } else if (id == R.id.nav_ECG) {
            Intent i = new Intent(Main2Activity.this, ECGActivity.class);
            startActivity(i);

        } else if (id == R.id.nav_PPG) {

            setTitle("PPG");

            ppg p=new ppg();
            FragmentManager fm=getSupportFragmentManager();

            fm.beginTransaction().replace(R.id.fragment,p).commit();



        } else if (id == R.id.nav_heartrate) {
            Intent i = new Intent(Main2Activity.this, HeartRateActivity.class);
            startActivity(i);

        }
        else if (id == R.id.nav_logout) {
           Logout();


        }
        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }
    public void ppgCam(View view) {
        Intent videoIntent=new Intent(MediaStore.ACTION_VIDEO_CAPTURE);

        if(videoIntent.resolveActivity(getPackageManager())!=null)
        {
            startActivityForResult(videoIntent,VIDEO_REQUEST);
        }
    }

    public void ShowPopup(View v) {
        TextView txtclose;
        myDialog.setContentView(R.layout.popup);
        txtclose =(TextView) myDialog.findViewById(R.id.txtclose);
        txtclose.setText("X");
        txtclose.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                myDialog.dismiss();
            }
        });
        myDialog.getWindow().setBackgroundDrawable(new ColorDrawable(Color.TRANSPARENT));
        myDialog.show();

    }

